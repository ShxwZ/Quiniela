    public void aniadirValores (){
        int Matriz [][] = inicio.getMatrix();
        List<Partido> partidos = inicio.getPartidos();
        int contador = 0;
        for (int i = 0; i < partidos.size(); i++) {
            String resultado = 
                    partidos.get(i).getGolesEquipo1() > partidos.get(i).getGolesEquipo2() ?
                    "Gana equipo 1" : 
                    partidos.get(i).getGolesEquipo1() < partidos.get(i).getGolesEquipo2() ?
                    "Gana equipo 2" : "Empate";
            String apuesta =
                    Matriz[i][0] == 1 ? "Gana equipo 1":
                    Matriz[i][2] == 1 ? "Gana equipo 2": "Empate";
            String resultadoApuesta = 
                    resultado.equals(apuesta) ? "CORRECTO" : "INCORRECTO";
            contador += resultadoApuesta.equals("CORRECTO") ? 1 :0;
            ((DefaultTableModel)jTableApuestas.getModel()).addRow(
                    new String[] {partidos.get(i).getEquipo1() + " - " + partidos.get(i).getEquipo2(),
                                  partidos.get(i).getGolesEquipo1()+"",
                                  partidos.get(i).getGolesEquipo2()+"",
                                  resultado,
                                  resultadoApuesta});
        }
        int apostado = Integer.parseInt(inicio.getjSpinnerApuesta().getValue().toString());
       
        String resultado7 = 
                partidos.get(6).getGolesEquipo1() > partidos.get(6).getGolesEquipo2() ?
                "Gana equipo 1" : 
                partidos.get(6).getGolesEquipo1() < partidos.get(6).getGolesEquipo2() ?
                    "Gana equipo 2" : "Empate";
        System.out.println(contador);
        String apuesta7 =
                    Matriz[6][0] == 1 ? "Gana equipo 1":
                    Matriz[6][2] == 1 ? "Gana equipo 2": "Empate";
        int premioFinal = premio(contador,resultado7,apostado,apuesta7);
        
        jLabelCantidadApostada.setText("Has ganado: " + premioFinal + "â‚¬");
    }
    public int premio(int contador,String resultado7,int apostado, String apuesta7){
        if (apuesta7.equals(resultado7) && contador == 7) {
            return apostado * 100;
        }
        if (apuesta7.equals(resultado7)) contador--;
        return contador == 5 ? apostado*10:
            contador == 6 ? apostado * 20:
                0;
        
    }
