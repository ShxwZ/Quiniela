    private void inicializarVariables(){
        equipos = new String [] {"Equipo 1","Equipo 2","Equipo 3","Equipo 4","Equipo 5","Equipo 6"};
        for (int[] Matrix1 : Matrix) {
            for (int j = 0; j < Matrix1.length; j++) {
                Matrix1[j] = 0;
                System.out.print(Matrix1[j]);
            }
            System.out.println("");
        }
        
        casillas = new JRadioButton[][] {{R00,R01,R02},{R10,R11,R12},
                                         {R20,R21,R22},{R30,R31,R32},
                                         {R40,R41,R42},{R50,R51,R52},
                                         {R60,R61,R62}};
        
        for (int i = 0; i < casillas.length; i++) {
            for (int j = 0; j < casillas[i].length; j++) {
                MapCasillas.put(casillas[i][j], i + "" + j);
            }
        }
        partidos = Logica.generarPartidos(equipos, 7);
        jlabels = new JLabel [] {jLabelPartido1,jLabelPartido2,jLabelPartido3,
                                jLabelPartido4,jLabelPartido5,jLabelPartido6,
                                jLabelPartido7};
        for (int i = 0; i < jlabels.length; i++) {
            jlabels[i].setText(partidos.get(i).getEquipo1() + " - "+partidos.get(i).getEquipo2() );
        }
    }
    private void aniadirEventos(){
        for (Map.Entry<JRadioButton, String> boton : MapCasillas.entrySet()) {
            boton.getKey().addActionListener((ActionEvent e) -> {
                registarApuesta(e);
            });
        }
    }

    public List<Partido> getPartidos() {
        return partidos;
    }
    
    public void registarApuesta(ActionEvent e){
        JRadioButton radioButton;
        String nombre = "";
        for (Map.Entry<JRadioButton, String> casilla : MapCasillas.entrySet()) {
            if (e.getSource() == casilla.getKey()) {
                radioButton = casilla.getKey();
                nombre = casilla.getValue();
            }
        }
        int fila =  Character.getNumericValue((char)nombre.charAt(0));
        int columna = Character.getNumericValue((char)nombre.charAt(1));
        Matrix[fila][0] = 0;
        Matrix[fila][1] = 0;
        Matrix[fila][2] = 0;
        Matrix[fila][columna] = 1;
        System.out.println(Matrix[fila][0] + " " + Matrix[fila][1] +
                " " + Matrix[fila][2]);     
        comprobacionRegistrar();
        
    }

